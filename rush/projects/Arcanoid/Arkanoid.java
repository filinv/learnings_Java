public class Arkanoid {
    private int width, height;
    
    public Arkanoid(int width, int height) {
        this.width = width;
        this.height = height;
    }
    public static void main(String[] args){}

    public int getWidth() {
        return width;
    }
    public int getHeight() {
        return height;
    }
    public void setWidth(int width) {
        this.width = width;
    }
    public void setHeight(int height) {
        this.height = height;
    }
}
